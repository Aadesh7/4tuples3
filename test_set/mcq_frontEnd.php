<div class="content" id="toefl">
    <h2>TOEFL</h2><br>
    <p id="toefl_text">The Test of English as a Foreign Language (internet-based test) is designed to measure whether someone is prepared to live and thrive in an English-speaking environment. Over 1 million people worldwide opt to take an English-speaking proficiency exam each year in order to attend an English-speaking university or simply as a refresher course to improve their English skills. TOEFL scores are often used by universities and colleges as an admission requirement for students who have received their education in a non-English speaking country.

        The TOEFL exam consists of the following four sections:</p>
    <h4> Reading</h4>

    <p id="last">The reading section of the TOEFL exam includes two main question types: Multiple Choice and Drag-and-Drop. This section contains between 36-56 questions and is 60-80 minutes in length.</p>
    <h4>Listening</h4>

        <p>The Listening section of the TOEFL exam contains similar types of questions as the reading section (Multiple Choice and Drag-and-Drop). This section has between 34-51 questions and is approximately 60-90 minutes in length.
            Speaking

            Test takers are provided with a microphone to record themselves responding to two types of prompts in the speaking section. Two tasks in prompt one consist of independent responses, and four tasks in prompt two require test takers to respond to a series of conversations, lectures and reading passages. Test takers have roughly one minute per prompt to record their responses. In total, the speaking section takes 20 minutes to complete.</p>
    <h4> Writing</h4>

        The exam ends with the writing section, which requires test takers to compose written responses to two tasks: one independent task, and one task in which a response to reading and listening material is composed. This section consists of two essays and is 50 minutes long</h4>
